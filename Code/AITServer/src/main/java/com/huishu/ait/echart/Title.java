public class Title {
	
	private String text;
	
	private String subText;

	public String getText() {
		return text;
	}

	public Title setText(String text) {
		this.text = text;
		return this;
	}

	public String getSubText() {
		return subText;
	}

	public Title setSubText(String subText) {
		this.subText = subText;
		return this;
	}
	
}
